// Helper function to get image by key
export function getImage(key: keyof typeof IMAGE_REGISTRY): ImageInfo {
  return IMAGE_REGISTRY[key];
}

// Helper function to get images by location/prefix
export function getImagesByLocation(location: string): ImageInfo[] {
  return Object.keys(IMAGE_REGISTRY)
    .filter(key => key.startsWith(location))
    .map(key => IMAGE_REGISTRY[key as keyof typeof IMAGE_REGISTRY]);
}

// Type-safe image keys
export type ImageKey = keyof typeof IMAGE_REGISTRY;